package academy.everyonecodes.java.assignment.exercise3;

public class Racecar {
    int speed;
    int damge;

    public Racecar(int speed, int damge) {
        this.speed = speed;
        this.damge = damge;
    }

    public int getSpeed() {
        return speed;
    }

    public void setSpeed(int speed) {
        this.speed = speed;
    }

    public int getDamge() {
        return damge;
    }

    public void setDamge(int damge) {
        this.damge = damge;
    }
}
